String = " I failed my way to success "

#Upper Case
string_uppercase = String.upper()

#Lower Case
string_lowercase = String.lower()

#Spliting
string_Split = String.split()

#Length
string_length = len(String)

print("String Sentence:", String)
print("Uppercase Sentence:", string_uppercase)
print("Lowercase Sentence:", string_lowercase)
print("List of Words:", string_Split)
print("Length of Sentence:", string_length)


